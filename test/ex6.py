# 비교 연산자 : 두 변수나 값을 비교하는 연산
var1 = 100    # int  100을 var1에 저장
var2 = "100"  # srt  문자 "100"을 var2에 저장

print("var1==var2 : ", (var1==var2))     # var1과 var2가 같은지 비교
print("var1!=var2 : ", (var1!=var2))     # var1과 var2가 다른지 비교
print("var1==var2 : ", str(var1)==var2)

a = 20
b = 7
print("a > b", (a>b))       # 초과
print("a < b :", (a<b))     # 미만
print("a >= 20 :", (a>=20)) # 이상
print("a <= 30 : ", (a<=30)) # 미만